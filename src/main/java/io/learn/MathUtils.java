package io.learn;

public class MathUtils {

    protected int add(int a,int b)
    {
        return a+b;
    }

    protected double computeCircleArea(double radius)
    {
        return 3.14 * radius * radius;
    }
    protected int divide(int num1,int num2)
    {
        return (num1)/(num2);
    }


    protected int multiply(int a,int b)
    {
        return a * b;
    }

}
